template <typename T> struct Num {
  T x;
  Num() { cin >> x; }
  Num(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};
using Int = Num<int>;

struct Str : string {
  Str() { cin >> *this; }
};

void solve(int t) {
  Int n, k;
  vector<Str> g(n);
  string row(n / k, ' ');
  for (int i = 0; i < n; i += k) {
    for (int j = 0; j < n; j += k) {
      row[j / k] = g[i][j];
    }
    println(row);
  }
}

int main() {
  cin.tie(nullptr)->tie(nullptr)->sync_with_stdio(false);
  Int t;
  for (int i = 1; i <= t; i++) {
    solve(i);
  }
}
