template <typename T> struct Num {
  T x;
  Num() { cin >> x; }
  Num(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};
using Int = Num<int>;

void solve(int t) {
  Int n;
  vector<Int> a(n);
  map<int, int> cnt;
  for (auto &&ai : a) {
    cnt[ai]++;
  }
  int mx = 0;
  for (auto &&[_, c] : cnt) {
    mx = max(mx, c);
  }
  println(n - mx);
}

int main() {
  cin.tie(nullptr)->tie(nullptr)->sync_with_stdio(false);
  Int t;
  for (int i = 1; i <= t; i++) {
    solve(i);
  }
}
